        /// </summary>
        TopCenter,

        /// <summary>
        /// Anchor point at the top right corner.
        /// </summary>
        TopRight,

        /// <summary>
        /// Anchor point at the middle left.
        /// </summary>
        MiddleLeft,

        /// <summary>
        /// Anchor point at the middle center.
        /// </summary>
        MiddleCenter,

        /// <summary>
        /// Anchor point at the middle right.
        /// </summary>
        MiddleRight,

        /// <summary>
        /// Anchor point at the bottom left corner.
        /// </summary>
        BottomLeft,

        /// <summary>
        /// Anchor point at the bottom center.
        /// </summary>
        BottomCenter,

        /// <summary>
        /// Anchor point at the bottom right corner.
        /// </summary>
        BottomRight
    }
}
